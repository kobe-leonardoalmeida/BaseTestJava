package models;

public class Elegibilidade {

    public String globoId;
    public String cpf;
    public String productCode;
    public String channel;
    public String msisdn;

    public Elegibilidade(String globoId, String cpf, String productCode, String channel, String msisdn) {
        this.globoId = globoId;
        this.cpf = cpf;
        this.productCode = productCode;
        this.channel = channel;
        this.msisdn = msisdn;
    }


}
